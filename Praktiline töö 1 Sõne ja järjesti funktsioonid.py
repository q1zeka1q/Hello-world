# Напиши программу, иллюстрирующую возможности применения функций для работы со списками и строками.
while True:
    print("\n--- Меню ---")
    print("1. Функции для работы со строками")
    print("2. Функции для работы со списками")
    print("3. Выход")
    выбор = input("Выберите пункт меню: ")

    if выбор == "1":
        print("\n--- Функции для работы со строками ---")
        строка = "hello world"

        # 1. Преобразование строки в верхний регистр
        print("В верхний регистр:", строка.upper(), "- Преобразует все буквы строки в заглавные.")

        # 2. Преобразование строки в нижний регистр
        print("В нижний регистр:", строка.lower(), "- Преобразует все буквы строки в строчные.")

        # 3. Проверка, начинается ли строка с определённой подстроки
        print("Начинается с 'hello':", строка.startswith("hello"), "- Проверяет начало строки.")

        # 4. Проверка, заканчивается ли строка определённой подстрокой
        print("Заканчивается на 'world':", строка.endswith("world"), "- Проверяет конец строки.")

        # 5. Нахождение позиции подстроки
        print("Позиция 'world':", строка.find("world"), "- Возвращает индекс первого вхождения подстроки.")

        # 6. Замена одной подстроки на другую
        print("Замена 'world' на 'Python':", строка.replace("world", "Python"), "- Заменяет подстроку.")

        # 7. Разделение строки на список по пробелам
        print("Разделение строки:", строка.split(), "- Разбивает строку на список по разделителю.")

        # 8. Объединение списка в строку с пробелом
        print("Объединение списка ['Python', 'is', 'awesome']:", " ".join(["Python", "is", "awesome"]), "- Объединяет список в строку.")

        # 9. Проверка, состоит ли строка только из букв
        print("Содержит только буквы ('hello'):", "hello".isalpha(), "- Проверяет, что строка состоит только из букв.")

        # 10. Преобразование строки: первая буква заглавная
        print("С заглавной буквы:", строка.capitalize(), "- Делает первую букву заглавной, остальные строчными.")

    elif выбор == "2":
        print("\n--- Функции для работы со списками ---")
        список = [1, 2, 3, 4, 5]

        # 1. Добавление элемента в конец списка
        список.append(6)
        print("Добавить 6:", список, "- Добавляет элемент в конец списка.")

        # 2. Вставка элемента по индексу
        список.insert(2, 99)
        print("Вставить 99 на позицию 2:", список, "- Вставляет элемент на заданную позицию.")

        # 3. Удаление элемента по значению
        список.remove(99)
        print("Удалить 99:", список, "- Удаляет первый найденный элемент с указанным значением.")

        # 4. Удаление элемента по индексу
        удалённый = список.pop(2)
        print("Удалить элемент на позиции 2:", удалённый, ", Список:", список, "- Удаляет элемент по индексу и возвращает его.")

        # 5. Сортировка списка
        список = [3, 1, 4, 2, 5]
        список.sort()
        print("Отсортированный список:", список, "- Сортирует элементы списка по возрастанию.")

        # 6. Обратный порядок элементов списка
        список.reverse()
        print("Обратный порядок:", список, "- Переворачивает список.")

        # 7. Нахождение индекса элемента
        print("Индекс элемента 4:", список.index(4), "- Возвращает индекс первого вхождения элемента.")

        # 8. Подсчёт количества вхождений элемента
        print("Количество вхождений элемента 4:", список.count(4), "- Считает количество вхождений элемента в списке.")

        # 9. Расширение списка другим списком
        список.extend([6, 7, 8])
        print("Расширить список [6, 7, 8]:", список, "- Добавляет элементы из другого списка.")

        # 10. Очистка списка
        список.clear()
        print("Очистить список:", список, "- Удаляет все элементы из списка.")

    elif выбор == "3":
        print("Выход из программы.")
        break
    else:
        print("Неверный выбор, попробуйте снова.")

